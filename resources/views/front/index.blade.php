    </div>

    <div class="post-content" id="post-content" itemprop="postContent">

        <p>使用 gitcafe pages 更新博客只需要敲几条命令，如果因为博客搬到了阿里云，却没有原来操作方便，那便得不偿失了。<br>所以就准备在服务器上建个 Git 远程仓库，用 Git Hooks 实现自动部署。</p>
<h2 id="Git-Hooks"><a href="http://www.imys.net/#Git-Hooks" class="headerlink" title="Git Hooks"></a>Git Hooks</h2><p>Git Hooks 就是一些触发特定事件的脚本。比如 commit、push、merge 等等，也区分本地 Hooks 和服务端 Hooks。<br>我这次使用的是<code>post-receive</code>。</p>
<blockquote>
<p>当用户在本地仓库执行<code>git-push</code>命令时，服务器上远程仓库就会对应执行<code>git-receive-pack</code>命令，而<code>git-receive-pack</code>命令会调用<code>pre-receive</code>钩子。</p>
</blockquote>
<p>这需要在服务器上建立一个 Git 远程仓库，和一个用于获取更新（git pull）的本地仓库。</p>
<p>远程仓库：最原始的版本库，所有的本地仓库就可以克隆这个仓库。就像你在 Github 创建一个项目，这就是一个远程仓库。<br>本地仓库：这个就是平常的工作目录，容易理解。<br>

    </p></div>

